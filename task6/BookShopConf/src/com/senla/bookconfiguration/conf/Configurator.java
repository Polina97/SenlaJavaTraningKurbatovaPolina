package com.senla.bookconfiguration.conf;

public class Configurator {
	private Integer monthOld;
	private Boolean isApplication;



	public Integer getMonthOld() {
		return monthOld;
	}

	public void setMonthOld(Integer monthOld) {
		this.monthOld = monthOld;
	}

	public Boolean getIsApplication() {
		return isApplication;
	}

	public void setIsApplication(Boolean isApplication) {
		this.isApplication = isApplication;
	}

}
